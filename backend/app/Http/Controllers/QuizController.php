    public function generateAndStoreQuiz(Request $request): JsonResponse
    {
        try {
            $validated = $request->validate([
                'lesson_id'       => 'required|exists:lessons,id',
                'title'           => 'required|string|max:255',
                'questions_count' => 'nullable|integer|min:3|max:25',
            ]);

            $lesson = Lesson::findOrFail($validated['lesson_id']);
            $cleanContent = mb_substr(strip_tags($lesson->content), 0, 5000);
            $count = $validated['questions_count'] ?? 5;

            if (!config('services.openai.key')) {
                throw new Exception("La clé OPENAI_API_KEY est manquante dans votre fichier .env ou config/services.php");
            }

            $questions = $this->fetchAiQuestions($cleanContent, $lesson->title, $count);

            $quiz = DB::transaction(function () use ($lesson, $validated, $questions) {
                $quiz = Quiz::create([
                    'lesson_id'     => $lesson->id,
                    'title'         => $validated['title'],
                    'description'   => "Auto-generated by AI based on: {$lesson->title}",
'passing_score' => $validated['passing_score'] ?? 70,
                    'questions_count' => count($questions),
                    'is_published'  => true,
                ]);

                foreach ($questions as $idx => $q) {
                    if (!isset($q['question_text'], $q['options'])) continue;

                    $quiz->questions()->create([
                        'question_text'  => $q['question_text'],
                        'question_type'  => $q['question_type'] ?? 'multiple_choice',
                        'difficulty'     => $q['difficulty'] ?? 'medium',
                        'correct_answer' => $q['correct_answer'] ?? 'A) Error',
                        'options'        => is_array($q['options']) ? json_encode($q['options']) : $q['options'],
                        'points'         => $this->calculatePoints($q['difficulty'] ?? 'medium'),
                        'order'          => $idx,
                    ]);
                }
                return $quiz->load('questions');
            });

            return response()->json([
                'success'  => true,
                'id'       => $quiz->id,
                'quiz_id'  => $quiz->id,
                'quiz'     => $quiz,
                'message'  => 'Quiz generated successfully!'
            ], 201);

        } catch (Throwable $e) {
            Log::error('QUIZ_GENERATION_ERROR', [
                'msg'   => $e->getMessage(),
                'file'  => $e->getFile(),
                'line'  => $e->getLine(),
                'trace' => $e->getTraceAsString()
            ]);

            $message = $e->getMessage();
            if (strpos($message, 'quota') !== false || strpos($message, '429') !== false) {
                $message = "OpenAI API quota exceeded. Fallback questions are being used.";
            } elseif (strpos($message, 'API key') !== false) {
                $message = "OpenAI API key not configured. Using fallback questions.";
            } elseif (empty($message)) {
                $message = "An error occurred during quiz generation.";
            }

            return response()->json([
                'success' => false,
                'error'   => 'Quiz Generation Error',
                'message' => $message,
            ], 500);
        }
    }

    private function fetchAiQuestions(string $content, string $title, int $count): array
    {
        try {
            $apiKey = config('services.openai.key');
            if (!$apiKey || empty(trim($apiKey))) {
                throw new Exception("OpenAI API key is not configured in .env file");
            }

            $systemPrompt = <<<'PROMPT'
You are an expert educational quiz creator. Generate high-quality quiz questions based on provided content.
Return ONLY valid JSON. No markdown, no code blocks, just pure JSON.
PROMPT;

            $userPrompt = <<<PROMPT
Create exactly $count quiz questions about "$title" based on this content:

$content

Generate questions with:
1. Mix of difficulty levels (easy, medium, hard)
2. Clear, specific questions
3. 4 multiple choice options (A, B, C, D)
4. Exactly ONE correct answer

Return JSON in this exact format:
{
  "questions": [
    {
      "question_text": "Question here?",
      "question_type": "multiple_choice",
      "difficulty": "medium",
      "options": ["A) Option 1", "B) Option 2", "C) Option 3", "D) Option 4"],
      "correct_answer": "B) Option 2"
    }
  ]
}

IMPORTANT:
- Generate exactly $count questions
- Ensure all options are different
- Make correct_answer match one of the options exactly
- Vary difficulty: easy (30%), medium (50%), hard (20%)
- Questions should test understanding, not just recall
PROMPT;

            $response = Http::withToken($apiKey)
                ->timeout(120)
                ->retry(2, 100)
                ->post('https://api.openai.com/v1/chat/completions', [
                    'model'           => 'gpt-4o-mini',
                    'messages'        => [
                        ['role' => 'system', 'content' => $systemPrompt],
                        ['role' => 'user',   'content' => $userPrompt],
                    ],
                    'response_format' => ['type' => 'json_object'],
                    'temperature'     => 0.7,
                    'max_tokens'      => 3000,
                ]);

            if (!$response->successful()) {
                $status = $response->status();
                $body   = $response->body();

                Log::error('OpenAI API Error', [
                    'status' => $status,
                    'body'   => substr($body, 0, 500)
                ]);

                if ($status === 429) {
                    throw new Exception("OpenAI API rate limited or quota exceeded. Please try again later or check your API quota.");
                } elseif ($status === 401) {
                    throw new Exception("Invalid OpenAI API key. Please check your .env configuration.");
                } else {
                    throw new Exception("OpenAI API error ({$status}): " . substr($body, 0, 200));
                }
            }

            $data = $response->json();

            if (!isset($data['choices'][0]['message']['content'])) {
                throw new Exception("Invalid OpenAI response format - missing content");
            }

            $content = $data['choices'][0]['message']['content'];
            $decoded = json_decode($content, true);

            if (!$decoded || !isset($decoded['questions']) || empty($decoded['questions'])) {
                throw new Exception("No questions generated from OpenAI response");
            }

            return $decoded['questions'];

        } catch (\Exception $e) {
            Log::error('Quiz AI Generation Failed', [
                'error' => $e->getMessage(),
                'file'  => $e->getFile(),
                'line'  => $e->getLine()
            ]);

            return $this->generateMockQuestions($title, $count);
        }
    }

