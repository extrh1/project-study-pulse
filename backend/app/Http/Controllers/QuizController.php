    /**
     * Submit a quiz attempt
     */
    public function submitAttempt(Request $request, $quizId): JsonResponse
    {
        try {
            $quiz = Quiz::with('questions')->find($quizId);
            if (!$quiz) {
                return response()->json(['message' => 'Quiz not found'], 404);
            }

            $validated = $request->validate([
                'answers' => 'required|array',
            ]);

            // Normalise answer keys to strings so both "12" and 12 match $question->id
            $answers = array_combine(
                array_map('strval', array_keys($validated['answers'])),
                array_values($validated['answers'])
            );

            $score       = 0;
            $totalPoints = 0;

            foreach ($quiz->questions as $question) {
                $points       = (int) ($question->points ?? 1);
                $totalPoints += $points;

                $key = (string) $question->id;

                if (!array_key_exists($key, $answers)) {
                    continue; // question was skipped
                }

                $userAnswer    = strtolower(trim((string) $answers[$key]));
                $correctAnswer = strtolower(trim((string) ($question->correct_answer ?? '')));

                // Also accept just the letter prefix (e.g. "b" matching "b) option text")
                $letterOnly = strtolower(trim(explode(')', (string)($question->correct_answer ?? ''))[0]));

                if ($userAnswer === $correctAnswer || $userAnswer === $letterOnly) {
                    $score += $points;
                }
            }

            $percentage = $totalPoints > 0 ? (int) round(($score / $totalPoints) * 100) : 0;
            $passed     = $percentage >= (int) ($quiz->passing_score ?? 70);

            $earnedXp = (int) round($percentage) + ($passed ? 50 : 0);

            // answers cast is 'array' in the model — pass the raw PHP array, NOT json_encode'd
            $attempt = QuizAttempt::create([
                'user_id'          => auth()->id(),
                'quiz_id'          => (int) $quizId,
                'score'            => $percentage,      // int 0–100
                'max_score'        => 100,
                'passed'           => (bool) $passed,
                'answers'          => $answers,         // array — model cast handles encoding
                'earned_xp'        => $earnedXp,
                'completed_at'     => now(),
            ]);

            // Safety net: if DB somehow stored null, force-patch it
            if ($attempt->score === null) {
                Log::warning('QUIZ_ATTEMPT_SCORE_NULL', [
                    'quiz_id'     => $quizId,
                    'user_id'     => auth()->id(),
                    'percentage'  => $percentage,
                    'totalPoints' => $totalPoints,
                    'raw_score'   => $score,
                ]);
                $attempt->forceFill(['score' => $percentage])->save();
            }

            // Award XP to user
            $user = auth()->user();
            $user->xp = (int)($user->xp ?? 0) + $earnedXp;
            $user->save();

            $awardedBadges = BadgeService::checkQuizBadges($user, $attempt);


            BadgeService::checkXpBadges($user);
            BadgeService::checkLevelBadges($user);
            BadgeService::checkSubjectBadges($user);
            BadgeService::checkStreakBadges($user);

            $badgeNames = collect($awardedBadges)->pluck('name')->toArray();

            return response()->json([
                'message'        => $passed ? 'Quiz passed!' : 'Quiz failed',
                'score'          => $attempt->score,   // use model value (never null)
                'correct'        => $score,
                'total_points'   => $totalPoints,
                'passed'         => $passed,
                'attempt_id'     => $attempt->id,
                'awarded_badges' => $badgeNames,
                'badge_count'    => count($awardedBadges),
                'xp_earned'      => $earnedXp,
            ]);
        } catch (\Exception $e) {
            return response()->json(['message' => $e->getMessage()], 400);
        }
    }

